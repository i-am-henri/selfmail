"use client"

/**
 * The default text input.
 */
export function Input() {
    return (
        <input type="text" />
    )
}

export function PasswordInput() {
    return (
        <input type="password" />
    )
}

export function FileInput() {
    return (
        <input type="file" />
    )
}

export function NumberInput() {
    return (
        <input type="number" />
    )
}

export function UrlInput() {
    return (
        <input type="url" />
    )
}